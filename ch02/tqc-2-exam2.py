a=eval(input())
b=eval(input())
oper=input()


if(oper=="+"):
    print(a+b)
elif(oper=="-"):
    print(a-b)
        
elif(oper=="*"):
    print(a*b)
            
elif(oper=="/"):
    print(a/b)
                
elif(oper=="//"):
    print(a//b)
                    
elif(oper=="%"):
    print(a%b)
    